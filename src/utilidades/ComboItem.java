package utilidades;

/**
 *
 * @author Santiago
 */
public class ComboItem
{
    private final String key;
    private final byte value;

    public ComboItem(String key, byte value)
    {
        this.key = key;
        this.value = value;
    }

    @Override
    public String toString()
    {
        return key;
    }

    public String getKey()
    {
        return key;
    }

    public byte getValue()
    {
        return value;
    }
}